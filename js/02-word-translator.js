let french="Fr";
let Germany="De";
let Esperanto="Es";

var interPreter=prompt("Please select your language (Fr,De,Es)");

if(interPreter==french){

    document.write("Hello Word translated in French is: Bonjour le mond");
}
else if(interPreter==Germany) {
    document.write("Hello World translated in German is: Hallo Welt"); 
}
      
else if(interPreter==Esperanto){
    document.write("Hello World translated in Esperanto is: Hallo Welt");

}
else {
    document.write("Hello World");
}
